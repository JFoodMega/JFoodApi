
CREATE TABLE "clients" (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    login VARCHAR(60) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL,
    phone VARCHAR(30) NOT NULL UNIQUE,
    name VARCHAR(120)
);

CREATE TABLE IF NOT EXISTS "addresses" (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    city VARCHAR(100) NOT NULL,
    street VARCHAR(150) NOT NULL,
    house VARCHAR(20)  NOT NULL,
    apartments INTEGER,
    floor INTEGER,
    entrance INTEGER,
    client_id BIGINT NOT NULL,
    CONSTRAINT fk_address_client FOREIGN KEY (client_id) REFERENCES clients(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS "dishes" (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(150) NOT NULL,
    description TEXT,
    price INTEGER NOT NULL,
    calories INTEGER,
    weight_volume VARCHAR(60) NOT NULL,
    image_url VARCHAR(300),
    cuisine_type VARCHAR(50) NOT NULL,
    dish_type VARCHAR(50) NOT NULL,
    is_active BOOLEAN NOT NULL DEFAULT true
);

CREATE TABLE IF NOT EXISTS "dish_extras" (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    price INTEGER NOT NULL,
    dish_id BIGINT NOT NULL,
    CONSTRAINT fk_extra_dish FOREIGN KEY (dish_id) REFERENCES dishes(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS "couriers" (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    login VARCHAR(255) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL,
    phone VARCHAR(30) NOT NULL UNIQUE,
    name VARCHAR(120),
    is_available BOOLEAN NOT NULL DEFAULT false
);

CREATE TABLE IF NOT EXISTS "moderators" (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    login VARCHAR(255) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL,
    phone VARCHAR(30) NOT NULL UNIQUE,
    name VARCHAR(120)
);

CREATE TABLE IF NOT EXISTS "orders" (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    client_id BIGINT NOT NULL REFERENCES clients(id) ON DELETE RESTRICT,
    courier_id BIGINT REFERENCES couriers(id) ON DELETE SET NULL,
    moderator_id BIGINT REFERENCES moderators(id) ON DELETE SET NULL,
    status VARCHAR(40) NOT NULL,
    total_price INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    assigned_at TIMESTAMP
);

CREATE TABLE IF NOT EXISTS "order_dishes" (
    order_id BIGINT NOT NULL REFERENCES orders(id) ON DELETE CASCADE,
    dish_id  BIGINT NOT NULL REFERENCES dishes(id) ON DELETE RESTRICT,
    PRIMARY KEY (order_id, dish_id)
);
