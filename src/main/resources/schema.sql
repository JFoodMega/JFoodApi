DROP TABLE IF EXISTS order_dishes CASCADE;
DROP TABLE IF EXISTS orders CASCADE;
DROP TABLE IF EXISTS dishes CASCADE;
DROP TABLE IF EXISTS adresses CASCADE;
DROP TABLE IF EXISTS clients CASCADE;

CREATE TABLE "clients" (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    login VARCHAR(60) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL,
    phone VARCHAR(30) NOT NULL UNIQUE,
    name VARCHAR(120)
);

CREATE TABLE "adresses" (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    city VARCHAR(100) NOT NULL,
    street VARCHAR(150) NOT NULL,
    house VARCHAR(20) NOT NULL,
    appartments INTEGER,
    floor INTEGER,
    entrance INTEGER,
    client_id BIGINT NOT NULL,
    CONSTRAINT fk_address_client FOREIGN KEY (client_id) REFERENCES clients (id) ON DELETE CASCADE
);

CREATE TABLE "dishes" (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    weight_volume VARCHAR(60) NOT NULL,
    image_url VARCHAR(300),
    name VARCHAR(150) NOT NULL,
    price INTEGER,
    description TEXT
);

CREATE TABLE "orders" (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    client_id BIGINT NOT NULL REFERENCES clients(id) ON DELETE RESTRICT,
    status VARCHAR(40) NOT NULL,
    total_price INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL
);

CREATE TABLE "order_dishes" (
    order_id BIGINT NOT NULL REFERENCES orders(id) ON DELETE CASCADE,
    dish_id  BIGINT NOT NULL REFERENCES dishes(id) ON DELETE RESTRICT,
    PRIMARY KEY (order_id, dish_id)
);